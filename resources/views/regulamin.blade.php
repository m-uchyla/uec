                            1. Zasady ogólne
Gracze uczestniczący w rozgrywkach Counter-Strike: Global Offensive Underground Esport Cup powinni przestrzegać wymienionych poniżej zasad w każdym momencie. Gracz zapisując się do turnieju - potwierdza iż zrozumiał, akceptuje i będzie się stosował do wymienionych poniżej zasad. Jako że konsekwentnie wprowadzamy zmiany i ulepszenia w UEC, poniższe zasady nie są finalnymi. Część z nich, a nawet całość, może ulec zmianie, aktualizacji lub poprawce w dowolnym momencie. 
 
1.1. Założenia ogólne
Jeśli uczestnik łamie jedną lub więcej zasad zawartych w regulaminie, może to nieść ze sobą różnorodne konsekwencje w zależności od wielkości przewinienia. Jeżeli przewinienie zostało dokonane przez gracza podczas gry zespołowej, zarówno gracz, jak i jego drużyna ponoszą tego konsekwencje w tym samym wymiarze.
Po otrzymaniu 3 ostrzeżeń drużyna zostanie usunięta z udziału w turnieju bądź zbanowana na czas nieokreślony. Każda wcześniej zbanowana drużyna może zostać z czasem odbanowana przez jednego z organizatorów. 

1.2. Zapisy
Aby wziąć udział w rozgrywkach UEC, drużyna musi podać pewne informacje. Zarejestrować się na stronie: https://undergroundesportcup.pl/ i wypełnić informacje o sobie w zakładce Panel Użytkownika/ Edytuj profil.  Po czym założyć drużynę i zaprosić do niej resztę zawodników w zakładce Panel Użytkownika/Panel Główny/Utwórz drużynę. Każdy gracz, który chce wziąć udział w turnieju musi założyć konto na https://undergroundesportcup.pl i podać tam swoje dane oraz zaakceptować regulamin. Aby zaprosić zawodnika do drużyny należy podać e-mail przez który jest on zalogowany na stronie. Wszystkie dane podane przez graczy będą wykorzystane jedynie podczas transmisji turnieju.

Nazwy, które posiadają prawne zastrzeżenie wyłączności oraz nazewnictwo, które wyraźnie kłóci się z ogólnie przyjętymi normami dobrego zachowania nie będą akceptowane. Zespoły ich używające zostaną odrzucone. Loga zastrzeżone prawnie (copyrighted) oraz loga, które są sprzeczne z ogólnie przyjętymi zasadami dobrego wychowania, nie zostaną zaakceptowane. Uczestnicy zgłaszając się do turnieju i podając imię oraz nazwisko wyrażają zgodę na użycie ich imienia oraz nazwiska oraz zdjęcia podczas streama .
