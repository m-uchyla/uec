6.1.2. Niestawienie się
Uczestnicy mają maksymalnie 15 minut od wyznaczonej daty spotkania. Jeżeli mecz nie dojdzie do skutku, uczestnik, który nie mógł rozegrać meczu przegra spotkanie walkowerem i otrzyma 3 punkty karne. W przypadku jakichkolwiek opóźnień występujących w turnieju, uczestnik ma 15 minut na stawienie się na serwer od zakończonej godziny poprzedniego spotkania.
6.1.3. Serwer do gry
Większość turniejów rozgrywana jest na wysokiej jakości serwerach UEC(128 tick).  Nakazuje się aby obie strony przed rozpoczęciem meczu sprawdziły poprawność ustawień serwera. Wszelkie nieprawidłowości powinny zostać usunięte przed przystąpieniem do gry.
