5. Procedury meczowe
5.1. Pseudonim gracza i tag drużyny
Każdy gracz musi używać nicku takiego samego lub podobnego do tego wprowadzonego w swoim zgłoszeniu. Gracz nie ma prawa zmiany nicku w grze podczas meczów UEC. Protesty w sprawie naruszenia tego punktu regulaminu będą odrzucane. Jeżeli zmiana nicku lub brak tagu drużyny utrudnia pracę administracji, punkty karne mogą zostać nałożone na gracza lub drużynę.
5.1.1. Rozgrzewka
Przed rozpoczęciem spotkania obie strony muszą jasno zadeklarować gotowość do wystartowania spotkania. Jeżeli w czasie startu spotkania któryś z graczy ma chwilowe problemy, należy poinformować o tym przeciwnika i mecz zostanie ponownie rozpoczęty.
5.1.2. Liczba graczy
Mecz może zostać rozpoczęty tylko kiedy minimalna ilosć graczy jest dostępna na serwerze:
5on5: tylko 5on5 jest dozwolone
Tylko zawodnicy którzy byli w drużynie o początkowej dacie rozpoczęcia spotkania mogą wziąć udział w rozgrywce. Ta zasady dotyczą tylko ilości graczy muszących potwierdzić gotowość do rozegrania spotkania w czasie początkowego rozpoczęcia meczu. W przypadku opuszczenia serwera przez gracza, prosze spojrzec na zasade 5.2.3
5.1.3. Trener
Obecność trenera podczas spotkań rozgrywanych w trybach 5on5 jest dozwolona. Trener musi być wcześniej zgłoszony w formularzu . Od trenera jest również wymagane korzystanie z poprawnego konta gry. Nadużywanie funkcji trenera skutkować będzie punktami karnymi za niesportowe zachowanie.
5.2. Podczas meczu
5.2.1. Wybór strony
O wyborze stron decyduje proces banowania lub runda nożowa, której zwycięzca wybiera po której stronie chce rozpocząć spotkanie. Przedłużanie rundy nożowej może być ukarane. 
5.2.2. Crash serwera
Jeżeli crash serwera nastąpi przed ukończeniem 3 rund, serwer musi zostać zrestartowany a wynik przywrócony do stanu 0-0.
Jeżeli crash serwera nastąpi po ukończeniu 3 rund, połówka musi zostać przywrócona do stanu ostatniej w pełni rozegranej rundy oraz z startmoney 2000, następnie drużyna która przegrała ostatnią rundę przed crashem serwera wpisuje ‘kill’ w konsoli. Runda ta nie jest brana pod uwagę. Gra rozpoczyna się w momencie rozpoczęcia kolejnej rundy
5.2.3. Wyjście z gry
Jeżeli gracz wyjdzie z serwera/zgłosi problem przed dokonaniem pierwszego zabójstwa w rundzie lub przed minięciem 30 sekund rundy, runda zostanie powtórzona. Natomiast jeżeli gracz wyjdzie z serwera po dokonaniu pierwszego zabójstwa lub minięciu 30 sekund rundy, mecz zostanie zatrzymany po zakończeniu bieżącej rundy (patrz zasada używanie pauzy). 
Czas oczekiwania na brakującego zawodnika wynosi 10 minut. Jeżeli gracz lub jego zastępca wejdzie na serwer i obie drużyny zasygnalizują gotowość do rozpoczęcia spotkania, mecz może zostać wznowiony. W przypadku gdy brakujący gracz ani jego zastępca nie wejdzie w ciągu 10 minut, drużyna musi kontynuować spotkanie z brakującym graczem.
5.2.4. Zmiana graczy
Zmiana gracza może zostać dokonana w każdym momencie, ale przeciwna drużyna musi zostać o tym poinformowana. Jeżeli jest to konieczne, gra w sytuacji zmiany zawodnika może zostać zatrzymana (patrz zasada używania pauzy). Zmiana nie może trwać dłużej niż 5 minut. Po upłynięciu 5 minut mecz może zostać kontynuowany z brakującym graczem który może dołączyć do spotkania w każdym momencie. Nowy zawodnik który dołącza do meczu musi być zgłoszony przy zapisach..
5.2.5. Użycie pause
Jeżeli masz problem który uniemożliwia Ci rozgrywanie spotkania, masz możliwość zatrzymania spotkania poprzez !tech na czacie. Funkcja ta musi być użyta na początku rundy (podczas freezetime). Musisz poinformować przeciwnika z jakiego powodu używasz pauzy przed jej użyciem lub zaraz po wpisaniu komendy. Komenda !pause Jeżeli nie zostanie podany jasny powód zatrzymania spotkania, mecz może zostać wznowiony. Odpauzowywanie i pauzowanie bez powodu będzie skutkowało punktami karnymi (patrz niesportowe zachowanie). Jeżeli problem nie zostanie rozwiązany w ciągu 10 minut mecz można skorzystać z podpunktu 5.2. Jeżeli drużyna nie zdecyduje się na zmianę zawodnika mecz zostaje kontynuowany po 10 minutach pauzy.
5.2.6. Dogrywka
Dogrywka musi być rozegrana w systemie MR3 (maksimum 6 rozegranych rund) z początkowymi funduszami wynoszącymi $16,000 (startmoney 16000). Podczas rozpoczęcia dogrywki strony rozpoczynają ją po takiej samej stronie w jakiej kończyli regulaminowy czas gry. Po zakończeniu pierwszej połowy obie drużyny zamieniają się stronami. Dogrywka jest rozgrywana do momentu wyłonienia zwycięzcy.
5.2.7. Nielegalne działania
5.2.7.1. Bomba
Podkładanie bomby w sposób który uniemożliwia jej rozbrojenie jest zabronione. Miejsca w których gracz potrzebuje pomocy kolegi z drużyny są dozwolone. Nie stosowanie się do tej reguły skutkować będzie ukaraniem drużyny
 
 
 
5.2.7.2. Wspinanie się
Wspinanie się z pomocą kolegi z drużyny jest dozwolone. Niedozwolone są miejsca gdzie tekstury znikają w ścianach lub ziemi w przypadku gdzie wymagany jest specjalny ruch (np. skok).
5.2.7.3.1. Tekstury
   Przechodzenie przez ściany, podłogi czy dachy oraz "chodzenie po niebie" jest zabronione.
5.2.7.3.2. Granaty
Rzucanie granatów wewnątrz ściany jest zabronione. Rzucanie ich nad ścianami i dachami jest dozwolone.
5.2.7.3.3. Samobójstwa
Samobójstwa poprzez wpisanie komendy ‘kill’ w konsoli w celu skrócenia czasu rundy są surowo zabronione.
5.2.7.3.4. Inne bugi
Używanie błędów gry jest niedozwolone. Jeżeli gracz skorzysta z buga który nie jest wyszczególniony w regulaminie, administrator zadecyduje czy gracz zostanie ukarany.
5.3. Po meczu
5.3.1. Zgłaszanie protestu
Tylko gracze/drużyny biorące udział w meczu mają prawo do złożenia protestu meczowego do administratora. Protest będzie rozpatrzony do 24 godzin od zakończenia spotkania
5.3.2. Przerwanie gry
Jeżeli drużyna opuści mecz przed jego zakończeniem, wygrana zostanie przyznana drużynie która pozostała w pełnym składzie na serwerze ( tylko w wypadku gdy mecz jest w fazie która pozwala na wyłonienie końcowego wyniku).
 
