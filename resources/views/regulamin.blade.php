6.1.5. Dodawanie nowych zawodników
Wszyscy zawodnicy będący w drużynie w momencie oficjalnego startu spotkania mają prawo brać udział w meczu. Nie ma możliwości dodawania graczy po planowanym rozpoczęciu meczu bądź po potwierdzeniu gotowości w systemie meczowym. W rozgrywkach 5on5, jeśli drużyna posiada graczy rezerwowych to może ona dokonać dwóch zmian podczas spotkania. Naruszenie tej zasady może skutkować walkowerem..
6.1.6. Dyskwalifikacja
Aby utrzymać jak najmniejsze opóźnienia podczas turnieju administracja zastrzega sobie prawo do dyskwalifikacji uczestników z turnieju. To prawo zostanie użyte tylko w wypadku gdy uczestnik pokazuje jawny brak chęci do dokończenia spotkania lub nawet celowo blokuje ciąg dalszy turnieju. W szczególnych przypadkach administrator ma prawo zdyskwalifikować obu uczestników.
 
