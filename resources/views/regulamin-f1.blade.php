                                <br>
                                <dd>5.1. Pseudonim gracza i tag drużyny</dd>
                                <dd>Każdy gracz musi używać nicku takiego samego lub podobnego do tego wprowadzonego w swoim zgłoszeniu. Gracz nie ma prawa zmiany nicku w grze podczas meczów UEC. Protesty w sprawie naruszenia tego punktu regulaminu będą odrzucane. Jeżeli zmiana nicku lub brak tagu drużyny utrudnia pracę administracji, punkty karne mogą zostać nałożone na gracza lub drużynę.</dd>
                                <br>
                                <dd>5.1.1. Rozgrzewka</dd>
                                <dd>Przed rozpoczęciem spotkania obie strony muszą jasno zadeklarować gotowość do wystartowania spotkania. Jeżeli w czasie startu spotkania któryś z graczy ma chwilowe problemy, należy poinformować o tym przeciwnika i mecz zostanie ponownie rozpoczęty.</dd>
                                <br>
                                <dd>5.1.2. Liczba graczy</dd>
                                <dd>Mecz może zostać rozpoczęty tylko kiedy minimalna ilosć graczy jest dostępna na serwerze:
                                5on5: tylko 5on5 jest dozwolone
                                Tylko zawodnicy którzy byli w drużynie o początkowej dacie rozpoczęcia spotkania mogą wziąć udział w rozgrywce. Ta zasady dotyczą tylko ilości graczy muszących potwierdzić gotowość do rozegrania spotkania w czasie początkowego rozpoczęcia meczu. W przypadku opuszczenia serwera przez gracza, prosze spojrzec na zasade 5.2.3</dd>
                                <br>
                                <dd>5.1.3. Trener</dd>
                                <dd>Obecność trenera podczas spotkań rozgrywanych w trybach 5on5 jest dozwolona. Trener musi być wcześniej zgłoszony w formularzu . Od trenera jest również wymagane korzystanie z poprawnego konta gry. Nadużywanie funkcji trenera skutkować będzie punktami karnymi za niesportowe zachowanie.</dd>
                                <br>
                                <dd>5.2. Podczas meczu</dd>
                                <br>
                                <dd>5.2.1. Wybór strony</dd>
                                <dd>O wyborze stron decyduje proces banowania lub runda nożowa, której zwycięzca wybiera po której stronie chce rozpocząć spotkanie. Przedłużanie rundy nożowej może być ukarane. </dd>
                                <br>
                                <dd> 5.2.2. Crash serwera</dd>
                                <dd>Jeżeli crash serwera nastąpi przed ukończeniem 3 rund, serwer musi zostać zrestartowany a wynik przywrócony do stanu 0-0.
                                Jeżeli crash serwera nastąpi po ukończeniu 3 rund, połówka musi zostać przywrócona do stanu ostatniej w pełni rozegranej rundy oraz z startmoney 2000, następnie drużyna która przegrała ostatnią rundę przed crashem serwera wpisuje ‘kill’ w konsoli. Runda ta nie jest brana pod uwagę. Gra rozpoczyna się w momencie rozpoczęcia kolejnej rundy</dd>
                                <br>
                                <dd>5.2.3. Wyjście z gry</dd>
                                <dd>Jeżeli gracz wyjdzie z serwera/zgłosi problem przed dokonaniem pierwszego zabójstwa w rundzie lub przed minięciem 30 sekund rundy, runda zostanie powtórzona. Natomiast jeżeli gracz wyjdzie z serwera po dokonaniu pierwszego zabójstwa lub minięciu 30 sekund rundy, mecz zostanie zatrzymany po zakończeniu bieżącej rundy (patrz zasada używanie pauzy). 
                                Czas oczekiwania na brakującego zawodnika wynosi 10 minut. Jeżeli gracz lub jego zastępca wejdzie na serwer i obie drużyny zasygnalizują gotowość do rozpoczęcia spotkania, mecz może zostać wznowiony. W przypadku gdy brakujący gracz ani jego zastępca nie wejdzie w ciągu 10 minut, drużyna musi kontynuować spotkanie z brakującym graczem.</dd>
                                <br>
                                <dd>5.2.4. Zmiana graczy</dd>
                                <dd>Zmiana gracza może zostać dokonana w każdym momencie, ale przeciwna drużyna musi zostać o tym poinformowana. Jeżeli jest to konieczne, gra w sytuacji zmiany zawodnika może zostać zatrzymana (patrz zasada używania pauzy). Zmiana nie może trwać dłużej niż 5 minut. Po upłynięciu 5 minut mecz może zostać kontynuowany z brakującym graczem który może dołączyć do spotkania w każdym momencie. Nowy zawodnik który dołącza do meczu musi być zgłoszony przy zapisach..</dd>
                                <br>
                                <dd>5.2.5. Użycie pause</dd>
                                <dd>Jeżeli masz problem który uniemożliwia Ci rozgrywanie spotkania, masz możliwość zatrzymania spotkania poprzez !tech na czacie. Funkcja ta musi być użyta na początku rundy (podczas freezetime). Musisz poinformować przeciwnika z jakiego powodu używasz pauzy przed jej użyciem lub zaraz po wpisaniu komendy. Komenda !pause Jeżeli nie zostanie podany jasny powód zatrzymania spotkania, mecz może zostać wznowiony. Odpauzowywanie i pauzowanie bez powodu będzie skutkowało punktami karnymi (patrz niesportowe zachowanie). Jeżeli problem nie zostanie rozwiązany w ciągu 10 minut mecz można skorzystać z podpunktu 5.2. Jeżeli drużyna nie zdecyduje się na zmianę zawodnika mecz zostaje kontynuowany po 10 minutach pauzy.</dd>
                                <br>
                                <dd>5.2.6. Dogrywka</dd>
