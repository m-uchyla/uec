                                <dt>5. Procedury wyścigowe</dt>
                                <br>
                                <dd>5.1. Pseudonim gracza </dd>
                                <dd>Każdy gracz musi używać nicku takiego samego lub podobnego do tego wprowadzonego w swoim zgłoszeniu. Gracz nie ma prawa zmiany nicku w grze podczas wyścigu UEC. Protesty w sprawie naruszenia tego punktu regulaminu będą odrzucane. Jeżeli zmiana nicku utrudnia pracę administracji, punkty karne mogą zostać nałożone na gracza lub drużynę.</dd>
                                <br>
                                <dd>5.2 Podczas wyścigu</dd>
                                <br>
                                <dd>5.2.1 Kwalifikacje</dd>
                                <dd>Przed wyścigiem rozgrywane są 18 minutowe kwalifikacje które decydują o pozycjach startowych kierowców.</dd>
                                <br>
                                <dd>5.2.2 Wyjście z gry</dd>
                                <dd>JJeżeli gracz wyjdzie z wyścigu ma prawo do niego ponownie dołączyć. Powinien o tym niezwłocznie poinformować administratora.</dd>
                                <br>
                                <dd>5.2.4 Wyścig</dd>
                                <dd>Po kwalifikacjach zostaje rozegrany wyścig. O pozycjach na starcie decydują kwalifikacje.</dd>
                                <br>
                                <dd>5.2.5 Nielegalne działania</dd>
                                <dd>ZWszelkie działania w grze oraz po za nią nie zgodne z ogólnie przyjmowaną netykietą będą karane. Sytuacje wyścigowe będą oceniane przez administratorów a decyzja o nałożeniu kary na zawodników podejmowana przez nich. W sytuacjach wyścigowych wzorujemy się na zasadach panujących w Formule 1 (biorąc margines błędu na to że ścigamy się w grze).</dd>
                                <br>
                                <dd>5.3. Po meczu</dd>
                                <br>
                                <dd>5.3.1. Zgłaszanie protestu</dd>
                                <dd>Tylko gracze biorący udział w wyścigu mają prawo do złożenia protestu. Protest będzie rozpatrzony do 72 godzin po zakończeniu wyścigu. Protesty składamy wraz z dowodami w formie filmu lub zdjęcia potwierdzające dane zdarzenie. Na przykład: Wyprzedzanie niezgodne z zasadami lub wulgarne wypowiadanie się o uczestniku.</dd>
                                <br>
                                <dt>6. Reguły turniejowe</dt>
                                <br>
                                <dd>Jeżeli nie podano inaczej, następujące reguły będą miały zastosowanie do wszystkich turniejów F1 2020.</dd>
                                <br>
                                <dd>6.1. Zasady specjalne</dd>
                                <br>
                                <dd>6.1.1. Zgłaszanie protestu</dd>
                                <dd>Czas na zgłoszenie protestu: w ciągu 24 godzin od zakończenia wyścigu.</dd>
                                <br>
                                <dd>6.1.2. Niestawienie się</dd>
                                <dd>Uczestnicy mają maksymalnie 10 minut od wyznaczonej daty spotkania. Jeżeli zawodnik nie stawi się w wyznaczonym czasie wyścig rozpocznie się bez danego kierowcy. Jeżeli kierowca nie poinformuje o tym wcześniej administratorów turnieju otrzyma 6 punktów karnych</dd>
                                <br>
                                <dd>6.1.3 Zastępstwa</dd>
                                <dd>W momencie w którym zawodnik nie będzie startował w wyścigu ma prawo zgłosić osobę która zastąpi go w danym wyścigu. Punkty które zdobędzie zawodnik który zastępuje daną osobą doliczane są do punktacji konstruktorów lecz w kwalifikacji kierowców jest uważany za odrębną osobę a punkty są doliczane na jego konto nie osobie którą zastępuje.</dd>
                                <br>
                                <dd>6.1.4 Dyskwalifikacja</dd>
                                <dd>Aby utrzymać jak najmniejsze opóźnienia podczas turnieju administracja zastrzega sobie prawo do dyskwalifikacji uczestników z turnieju. To prawo zostanie użyte tylko w wypadku gdy uczestnik pokazuje jawny brak chęci do dokończenia turnieju.</dd>
                                <br>
                                <dt>7. Kary, upomnienia oraz punkty karne</dt>
                                <br>
                                <dd>7.1 Upomnienia</dd>
                                <br>
                                <dd>7.1.1 Pierwsze upomnienie - nie niesie za sobą żadnych konsekwencji jest ostrzeżeniem dla zawodnika. </dd>
                                <br>
                                <dd>7.1.2 Drugie upomnienie podczas wyścigu oznacza dyskwalifikacje z wyścigu. Jeżeli drugie upomnienie zostanie nadane po wyścigu zawodnik jest wykluczony podczas następnego wyścigu.</dd>
                                <br>
                                <dd>7.1.3 trzecie upomnienie podczas turnieju oznacza dyskwalifikację zawodnika</dd>
                                <br>
                                <dd>7.2 Punkty karne</dd>
                                <dd>Za przewinienia podczas wyścigu nadawane są punkty karne od 1 do 3 (punkty karne nie wypływają na kwalifikacje kierowców lub konstruktorów i są oddzielną statystyką). Za każde przewinienie punkty nadawane są oddzielnie. Jeżeli zawodnik brał udział w wyścigu w więcej niż jednym incydencie punkty nałożone na każdy z nich sumują się. Po uzbieraniu 12 punktów karnych w sezonie zawodnik zostaje wykluczony z następnego wyścigu jaki nastąpi.</dd>
                                <br>
                                <dd>7.3 cheatowanie, używanie programów ingerujących w rozgrywkę lub silnik gry oraz innych tym podobnych rzeczy opisanych wyżej w regulaminie - dyskwalifikacja oraz ban dla zawodnika</dd>
                                <br>
                                <dd>7.4 Czyny zakazane podczas turnieju są opisane w powyższych punktach regulaminu</dd>
                                <br>
                                <dd>7.5 Drużyna może odwołać się od decyzji administratora w ciągu 24 godzin przedstawiając dowody lub inne fakty.</dd>
                                <br>
                                <dd>7.6 Ostateczna decyzja zawsze należy do administratorów i mogą oni wykluczyć zespół z turnieju za m.in nie sportowe zachowanie obrazę administratora itp.</dd>
                                <br>
                                <dd>7.7 Niesportowe zachowanie: obraża rywali lub administratora oraz specjalne przedłużanie gry będzie karane upomnieniem lub nawet dyskwalifikacją</dd>
                                
